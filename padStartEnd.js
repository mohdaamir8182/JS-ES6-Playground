//padStart() adds the certain character to the start of the string to complete its length.
//Example.....

let myName = "Aamir";

console.log("PAD START....:" , myName.padStart(10 , "a")) //PAD START....: aaaaaAamir

//padEnd() adds certain character to the end of an string to complete it's length.

console.log("PAD END....:" , myName.padEnd(10 , "a")) //PAD END....: Aamiraaaaa